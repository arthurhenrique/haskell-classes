--concat
[1,2]++[3,4]

--verifica se vazio
null []

--tail -> retorna calda da lista
tail [1,2,3]

--last -> retorna ultimo elemento da lista
last [1,2,3,4]

--init -> retorna lista sem ultimo elemento
init [1,2,3,4]

--head -> retorna primeiro elemento
head [1,2,3,4]

--length -> retorna quantidade de elementos da lista
length [1,2,3,4]

--drop -> apaga n primeiros elementos da lista
drop 2 [1,2,3,4]

--lines -> retorna lista de listas
lines "arthur"

--take -> retorna  n primeiro elementos da lista
take 1 [1,2,3,4,5]

--words -> retorna lista de palavras
words "Arthur Henrique"

--reverse -> retorna elementos reverso de uma lista
reverse "Arthur"

--repeat -> retorna uma lista potencialmente infinita
repeat [1,2]

--replicate -> retorna uma lista replicada n vezes
replicate 2 ["arthur"]

--cycle -> retorna uma lista potencialmente infinita
cycle [1]

--splitAt -> retorna uma lista cortada a partir de um parametro n
splitAt 6 "arthurhenrique"



